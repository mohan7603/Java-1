// ARITHMETIC OPERATOR

public class Arithmetic{
    public static void main(String args[])
    {
        // Create variables

        int a = 30;
        int b = 20;
         
         //Addition
        System.out.println("Addition       :"+(a+b));    // OUTPUT : 50
         //Subraction
        System.out.println("Subraction     :"+(a-b));   // OUTPUT : 10
        //Multiplication
        System.out.println("Multiplication :"+(a*b));   // OUTPUT : 600
         //Division
        System.out.println("Division       :"+(a/b));   // OUTPUT : 1
         //Modules
        System.out.println("Modules        :"+(a%b));   // OUTPUT : 10
         
    }
}